package jooseok;

public class switchcase {
	public static void main(String[] args) {
		int myGrade = 50;
		
		switch(myGrade){
			case 30:
				System.out.println("bad");
				break;
			case 50:
				System.out.println("good");
				break;
			case 70:
				System.out.println("great");
				break;
		}

	}
}
